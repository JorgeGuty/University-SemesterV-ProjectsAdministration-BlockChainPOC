    private void enrollmentInsertButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enrollmentInsertButtonActionPerformed
        URL url;
        try {
            url = new URL("http://127.0.0.1:5000/insertPayment?company_name="+this.enrollmentCompanyName.getText()
                        +"&crowsourcer_name="+this.enrollmentCrowdsourcerName.getText()
                        +"&microtask_name="+this.enrollmentCrowdsourcerName.getText());
            HttpURLConnection con = (HttpURLConnection) url.openConnection();
            con.setRequestMethod("POST");
            int responseCode = con.getResponseCode();
            if (responseCode == HttpURLConnection.HTTP_OK) this.enrollmentSuccessMessage.setText("Insertado con éxito!!!");
            con.disconnect();
        } catch (Exception ex) {
            Logger.getLogger(Front.class.getName()).log(Level.SEVERE, null, ex);
            this.enrollmentSuccessMessage.setText(ex.getLocalizedMessage());
        }
    }//GEN-LAST:event_enrollmentInsertButtonActionPerformed

