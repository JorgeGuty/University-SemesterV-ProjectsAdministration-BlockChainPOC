    private void getBlockChainButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getBlockChainButtonActionPerformed
        // TODO add your handling code here:
                // TODO add your handling code here:
        URL url;
        try {
            url = new URL("http://127.0.0.1:5000/chain");
            HttpURLConnection con = (HttpURLConnection) url.openConnection();
            con.setRequestMethod("GET");
            int responseCode = con.getResponseCode();
            if (responseCode == HttpURLConnection.HTTP_OK){
                BufferedReader in = new BufferedReader(
                new InputStreamReader(con.getInputStream()));
                String inputLine;
                StringBuffer content = new StringBuffer();
                while ((inputLine = in.readLine()) != null) {
                    content.append(inputLine);
                }
                in.close();
                
                String jsonResponse = content.toString();
                JSONObject json = new JSONObject(jsonResponse);
                this.BlockChainDisplayer.setText(json.toString(4));
            }
            con.disconnect();
        } catch (Exception ex) {
            Logger.getLogger(Front.class.getName()).log(Level.SEVERE, null, ex);
        }   
    }//GEN-LAST:event_getBlockChainButtonActionPerformed

